import turtle
turtle.begin_fill()
turtle.forward(100)
turtle.right(60)
turtle.backward(100)
turtle.left(120)
turtle.backward(100)
turtle.color("blue","red")

turtle.end_fill()
turtle.penup()
turtle.goto(100,100)
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(-300,20)
turtle.pendown()
turtle.setheading(0)
turtle.forward(50)
turtle.right(144)
turtle.forward(50)
turtle.right(144)
turtle.forward(50)
turtle.right(144)
turtle.forward(50)
turtle.right(144)
turtle.forward(50)

turtle.done()
